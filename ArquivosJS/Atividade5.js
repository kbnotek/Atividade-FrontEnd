/*Operador “+” em JavaScript: O operador “+” é usado para adicionar valores numéricos ou concatenar strings.*/
// Adição de números
let soma = 5 + 3;
console.log(soma);  // Saída: 8

// Concatenação de strings
let saudacao = 'Olá, ' + 'mundo!';
console.log(saudacao);  // Saída: Olá, mundo!

// Quando números e strings são usados juntos, o JavaScript converte os números em strings
let resultado = 'O resultado é: ' + 5;
console.log(resultado);  // Saída: O resultado é: 5
